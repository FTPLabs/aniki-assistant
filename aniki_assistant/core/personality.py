import random


def get_phrase(category: str, **kwargs) -> str:
    """Получить случайную фразу из категории."""
    phrases = GACHI_PHRASES.get(category, ["Понял, бро!"])
    phrase = random.choice(phrases)
    if kwargs:
        try:
            phrase = phrase.format(**kwargs)
        except KeyError:
            pass
    return phrase
