  def classify_request(text: str) -> str:
      """Определяет тип запроса для выбора агента."""
      t = text.lower()
      
      if any(w in t for w in ["код", "python", "java", "функц", "напиши код", "скрипт",
                                "ошибка", "баг", "debug", "программ", "алгоритм"]):
          return "code"
      
      if any(w in t for w in ["найди", "поищи", "что такое", "кто такой", "погода",
                                "новости", "сколько стоит", "расскажи про"]):
          return "search"
      
      if any(w in t for w in ["напиши стих", "придумай историю", "сочини", "рассказ",
                                "творчес", "стихотворен", "поэм"]):
          return "creative"
      
      if any(w in t for w in ["сколько", "посчитай", "реши", "математик", "уравнен",
                                "процент", "вычисл", "формул"]):
          return "math"
      
      if any(w in t for w in ["запомни", "не забудь", "я зовут", "моё имя",
                                "я работаю", "мне нравится"]):
          return "memory"
      
      if any(w in t for w in ["напомни", "напоминание", "поставь таймер", "через час"]):
          return "reminder"
      
      return "default"


