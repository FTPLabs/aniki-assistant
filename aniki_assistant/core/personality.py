def get_phrase(category, **kwargs):
    phrases = GACHI_PHRASES.get(category, ['Понял!'])
    phrase = random.choice(phrases)
    if kwargs:
        try:
            phrase = phrase.format(**kwargs)
        except KeyError:
            pass
    return phrase


def get_agent_prompt(agent_type):
    return AGENT_PROMPTS.get(agent_type, SYSTEM_PROMPT)


def classify_request(text):
    t = text.lower()
    if any(w in t for w in ['открой','запусти','закрой','выключи','перезагрузи',
            'громкость','яркость','скриншот','youtube','ютуб','браузер']):
        return 'command'
    if any(w in t for w in ['найди','поищи','погугли','что такое','кто такой',
            'сколько стоит','когда','где','почему']):
        return 'search'
    if any(w in t for w in ['запомни','помнишь','что ты знаешь','забудь',
            'моё имя','меня зовут']):
        return 'memory'
    if any(w in t for w in ['напиши','придумай','сочини','промпт',
            'стихотворение','сгенерируй','создай']):
        return 'creative'
    if any(w in t for w in ['мотивируй','поддержи','устал','не могу',
            'сложно','грустно']):
        return 'motivation'
    if any(w in t for w in ['объясни','расскажи','как работает',
            'что значит','чем отличается']):
        return 'knowledge'
    return 'chat'
